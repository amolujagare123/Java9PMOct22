package StringDemos;

public class StringDemo1 {

    public static void main(String[] args) {

        String str1 = "My name is Amol"; // 15
        System.out.println("length str1="+str1.length());
        System.out.println("character at index 4="+str1.charAt(4));

        String str2 = "Selenium"; // 8
        System.out.println("length str2="+str2.length());
        System.out.println("character at index 3="+str2.charAt(3));





    }
}
